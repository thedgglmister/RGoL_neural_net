"""This module contains the PrintColors class."""

class PrintColors:
    """Contains constant ansi codes for use in changing color of printed text.

    Included constants are:
    WHITE, RED, GREEN, YELLOW, BLUE, and CYAN."""
    WHITE  = '\033[0m'
    RED  = '\033[31m'
    GREEN  = '\033[32m' 
    YELLOW  = '\033[33m' 
    BLUE  = '\033[34m' 
    CYAN = '\033[36m'
